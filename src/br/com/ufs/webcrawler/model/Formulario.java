public class Formulario {

	int codigo;
	String data;
	String informacoesInstitucionais;
	String servicos;
	String comentarios;
	String corpoClinico;
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getInformacoesInstitucionais() {
		return informacoesInstitucionais;
	}
	public void setInformacoesInstitucionais(String informacoesInstitucionais) {
		this.informacoesInstitucionais = informacoesInstitucionais;
	}
	public String getServicos() {
		return servicos;
	}
	public void setServicos(String servicos) {
		this.servicos = servicos;
	}
	public String getComentarios() {
		return comentarios;
	}
	public void setComentarios(String comentarios) {
		this.comentarios = comentarios;
	}
	public String getCorpoClinico() {
		return corpoClinico;
	}
	public void setCorpoClinico(String corpoClinico) {
		this.corpoClinico = corpoClinico;
	}
	
		
	
}
